#include "lem_in.h"

int	strlen_space(char *str)
{
	int		i;

	i = 0;
	while (*str && ft_strchr(" \t", *str))
		str++;
	while (str[i] && ft_strchr("0123456789", str[i]))
		i++;
	return (i);
}
